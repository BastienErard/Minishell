#include "../minishell.h"

int	ft_isaldig(char *arg)
{
	int	i;

	i = -1;
	while (arg[++i] != '\0')
	{
		if (arg[i] < 48 || (arg[i] > 57 && arg[i] < 65)
			|| arg[i] > 122)
			return (0);
	}
	return (1);
}
